#!/usr/bin/env node
/**
 * Rulebook evals — snapshot generator.
 *
 * Regenerates `evals/snapshots/results.json` by calling the Anthropic
 * API for every (prompt, arm) combination. Expensive; requires an
 * `ANTHROPIC_API_KEY` environment variable and the `@anthropic-ai/sdk`
 * npm package.
 *
 * Design: separate this expensive step from `measure.ts` (cheap,
 * offline) so CI can run the measurement on every PR without API
 * credentials. Snapshots regenerate on-demand via this script or the
 * `evals-snapshot` GitHub Actions workflow.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

interface Arm {
  id: string;
  systemPrompt?: string;
  systemPromptPrefix?: string;
  skillFile?: string;
}

interface ArmsFile {
  arms: Arm[];
}

interface AnthropicMessagesAPI {
  create(params: {
    model: string;
    max_tokens: number;
    system?: string;
    messages: Array<{ role: 'user' | 'assistant'; content: string }>;
  }): Promise<{ content: Array<{ type: string; text?: string }> }>;
}

interface AnthropicClient {
  messages: AnthropicMessagesAPI;
}

async function loadAnthropic(): Promise<AnthropicClient | null> {
  try {
    // Dynamic import keeps the dependency optional.
    // @ts-expect-error — optional peer dep; absence handled by the catch below.
    const mod = (await import('@anthropic-ai/sdk')) as unknown as {
      default: new (opts: { apiKey: string }) => AnthropicClient;
    };
    const key = process.env.ANTHROPIC_API_KEY;
    if (!key) {
      console.error(
        'ANTHROPIC_API_KEY is not set. Export it or use the evals-snapshot GH Actions workflow.'
      );
      return null;
    }
    return new mod.default({ apiKey: key });
  } catch {
    console.error(
      'Cannot load @anthropic-ai/sdk. Install with: npm install --save-dev @anthropic-ai/sdk'
    );
    return null;
  }
}

function buildSystemPrompt(arm: Arm, root: string): string {
  if (arm.systemPromptPrefix) {
    const skillPath = arm.skillFile ? resolve(root, arm.skillFile) : '';
    const skillBody = skillPath ? readFileSync(skillPath, 'utf8') : '';
    return `${arm.systemPromptPrefix}\n\n${skillBody}`.trim();
  }
  return arm.systemPrompt ?? '';
}

async function main(): Promise<number> {
  const root = process.cwd();
  const client = await loadAnthropic();
  if (!client) return 1;

  const arms: ArmsFile = JSON.parse(
    readFileSync(resolve(root, 'evals/arms.json'), 'utf8')
  );
  const prompts = readFileSync(resolve(root, 'evals/prompts/en.txt'), 'utf8')
    .split('\n')
    .map((l) => l.trim())
    .filter((l) => l.length > 0);

  const model = process.env.ANTHROPIC_MODEL ?? 'claude-sonnet-4-6';
  console.log(`Model: ${model}`);
  console.log(`Arms:    ${arms.arms.map((a) => a.id).join(', ')}`);
  console.log(`Prompts: ${prompts.length}`);
  console.log('');

  const results: Array<{
    id: string;
    prompt: string;
    responses: Record<string, string>;
  }> = [];

  for (let i = 0; i < prompts.length; i++) {
    const prompt = prompts[i];
    const id = `p${i + 1}-${prompt
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .slice(0, 40)}`;
    console.log(`[${i + 1}/${prompts.length}] ${id}`);

    const responses: Record<string, string> = {};
    for (const arm of arms.arms) {
      const system = buildSystemPrompt(arm, root);
      const response = await client.messages.create({
        model,
        max_tokens: 1024,
        ...(system ? { system } : {}),
        messages: [{ role: 'user', content: prompt }],
      });
      const text = response.content
        .filter((b) => b.type === 'text')
        .map((b) => b.text ?? '')
        .join('');
      responses[arm.id] = text;
      console.log(`  ${arm.id}: ${text.length} chars`);
    }
    results.push({ id, prompt, responses });
  }

  const output = {
    generatedAt: new Date().toISOString(),
    model,
    note: `Generated by evals/llm_run.ts against the live API.`,
    prompts: results,
  };

  const outPath = resolve(root, 'evals/snapshots/results.json');
  writeFileSync(outPath, JSON.stringify(output, null, 2) + '\n');
  console.log(`\nWrote ${outPath}`);
  return 0;
}

import { fileURLToPath } from 'node:url';
if (process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1])) {
  main().then((code) => process.exit(code));
}
