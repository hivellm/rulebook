import { promises as fs } from 'fs';

/**
 * v6 → v7 migration cleanup (docs/analysis/v7-performance/ 4.7).
 *
 * v7 stopped generating a long list of always-on rules, hook scripts, spec
 * documents and retired-subsystem skills. `rulebook update` uses this module
 * to remove the rulebook-OWNED leftovers from existing projects — never
 * touching user-authored files — and to normalize spec filenames to the v7
 * lowercase convention. Pure planning is separated from application so
 * `--dry-run` can print the exact plan without writing anything.
 */

